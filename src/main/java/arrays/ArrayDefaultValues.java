package arrays;

public class ArrayDefaultValues {
    public static void main(String[] args) {

        int[] intNumbers = new int[3];
        System.out.println(intNumbers[0]);
        System.out.println(intNumbers[1]);
        System.out.println(intNumbers[2]);

        double[] doubleNumbers = new double[3];
        System.out.println(doubleNumbers[0]);
        System.out.println(doubleNumbers[1]);
        System.out.println(doubleNumbers[2]);

        String[] names = new String[3];
        System.out.println(names[0]);
        System.out.println(names[1]);
        System.out.println(names[2]);
    }
}
